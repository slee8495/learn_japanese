const WORDS_PER_DAY = 10;

function getDaysSinceStart() {
  const now = new Date();
  const start = new Date(2026, 5, 28); // 로컬 시간 기준
  const today = new Date(now.getFullYear(), now.getMonth(), now.getDate());
  return Math.max(0, Math.floor((today - start) / (1000 * 60 * 60 * 24)));
}

const ALL_WORDS = [...level1, ...level2, ...level3, ...level4, ...level5, ...level6, ...level7];

export function getUnlockedCount() {
  return Math.min((getDaysSinceStart() + 1) * WORDS_PER_DAY, ALL_WORDS.length);
}

export function getTodaysWords() {
  const day = getDaysSinceStart();
  const start = day * WORDS_PER_DAY;
  const end = start + WORDS_PER_DAY;
  return ALL_WORDS.slice(start, end);
}

export function getAllUnlockedWords() {
  return ALL_WORDS.slice(0, getUnlockedCount());
}

export function getTotalWordCount() {
  return ALL_WORDS.length;
}
